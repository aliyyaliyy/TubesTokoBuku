    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String posisi = ComboBoxPosition.getSelectedItem().toString();
        String password = new String(txtPassword.getPassword());
        
        if (posisi == null || txtPassword.getPassword().length == 0) {
            JOptionPane.showMessageDialog(null, "Posisi atau Password tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        ControllerLogin controllerLogin = new ControllerLogin();
        boolean isValid = controllerLogin.validateLogin(posisi, password);

        if (isValid) {
            JOptionPane.showMessageDialog(null, "Login berhasil sebagai " + posisi + "!");
            switch (posisi) {
                case "Owner":
                    new MenuOwner().setVisible(true);
                    this.dispose();
                    break;
                case "Admin":
                    new MenuAdmin().setVisible(true);
                    this.dispose();
                break;
                case "Kasir":
                    new MenuKasir().setVisible(true);
                    this.dispose();
                break;
                default:
                    JOptionPane.showMessageDialog(null, "Posisi tidak valid!", "Error", JOptionPane.ERROR_MESSAGE);
                break;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Password salah atau posisi tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

