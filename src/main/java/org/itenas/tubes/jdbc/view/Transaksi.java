    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        //ambil inputan buku dari txtField
        try {
            String namaBuku = txtNamaBuku.getText();
            int jumlah = Integer.parseInt(txtJumlah.getText());
            double totalHarga = Double.parseDouble(txtTotalHarga.getText());

            //melakukan validasi pada inputan
            if (namaBuku.isEmpty() || txtJumlah.getText().isEmpty() || txtTotalHarga.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Harap isi jumlah terlebih dahulu sebelum menambahkan", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            //Mengambil model dari tabel transaksi
            DefaultTableModel model = (DefaultTableModel) tabelTransaksi.getModel();
            
            //membuat idtransaksi secara otomatis
            int idTransaksi = model.getRowCount() + 1;

            //Menambahkan data ke tabel transaksi
            model.addRow(new Object[] {
                idTransaksi,
                namaBuku,
                jumlah,
                totalHarga
            });
            
            hitungTotalKeseluruhan();
            
            JOptionPane.showMessageDialog(null, "Item berhasil ditambahkan ke transaksi!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            clearData();
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(null, "Harap masukkan angka yang valid untuk jumlah buku yang dipilih", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

