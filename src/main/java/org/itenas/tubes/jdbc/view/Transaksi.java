    private void showDataBukuFromController() {
        DefaultTableModel model = (DefaultTableModel) tabelDataBuku.getModel();
        model.setRowCount(0);
        
        List<DataBuku> listBuku = conDataBuku.showAll();
        for (DataBuku buku : listBuku) {
            model.addRow(new Object[] {
                buku.getIsbn(),
                buku.getJudulBuku(),
                buku.getGenre(),
                buku.getPengarang(),
                buku.getTahunTerbit(),
                buku.getHarga(),
                buku.getStok()
            });
        } 
    }
    
