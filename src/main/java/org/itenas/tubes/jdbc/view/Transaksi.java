    private void tabelDataBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelDataBukuMouseClicked
        // TODO add your handling code here:
        int selectedRow = tabelDataBuku.getSelectedRow();
        
        if (selectedRow != -1) {
            String namaBuku = tabelDataBuku.getValueAt(selectedRow, 1).toString();
            String harga = tabelDataBuku.getValueAt(selectedRow, 5).toString();
            
            txtNamaBuku.setText(namaBuku);
            txtHarga.setText(harga);
        }
    }//GEN-LAST:event_tabelDataBukuMouseClicked

    private void txtJumlahKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtJumlahKeyReleased
        // TODO add your handling code here:
        try {
            //mengambil nilai dari txtHarga dan txtjumlah
            int jumlah = Integer.parseInt(txtJumlah.getText());
            double harga = Double.parseDouble(txtHarga.getText());
            
            
            //validasi stok
            int selectedRow = tabelDataBuku.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(null, "Pilih buku terlebih dahulu dari tabel!", "Error", JOptionPane.ERROR_MESSAGE);
                txtJumlah.setText("");
                txtTotalHarga.setText("");
                return;
            }
            
            int stok = Integer.parseInt(tabelDataBuku.getValueAt(selectedRow, 6).toString());
            
            if (jumlah > stok) {
                JOptionPane.showMessageDialog(null, "Jumlah melebihi stok yang tersedia!","Error", JOptionPane.ERROR_MESSAGE);
                txtJumlah.setText("");
                txtTotalHarga.setText("");
                return;
            }
            
            //menghitung total harga
            double totalHarga = jumlah * harga;
            
            //menampilkan hasil dari menghitung total harga ke txtTotalHarga
            txtTotalHarga.setText(String.valueOf(totalHarga));
        } catch (NumberFormatException nfe) {
            //mengosokan txtTotalharga kalo misalnya inputannya ga bener
            txtTotalHarga.setText("");
        }
    }//GEN-LAST:event_txtJumlahKeyReleased

