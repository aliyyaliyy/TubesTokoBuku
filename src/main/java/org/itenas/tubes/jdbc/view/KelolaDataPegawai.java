    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String keyword = txtSearch.getText().trim();
        if (keyword.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Masukkan kata kunci untuk mencari!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        List<DataPegawai> hasilPencarian = conPegawai.findByIdNamePosisi(keyword);
        
        DefaultTableModel dtm = (DefaultTableModel) tabelDataPegawai.getModel();
        dtm.setRowCount(0);
        
        for (DataPegawai pegawai : hasilPencarian) {
            Object[] data = {
                pegawai.getIdPegawai(),
                pegawai.getNamaPegawai(), 
                pegawai.getEmail(), 
                pegawai.getPosisi(),
                pegawai.getPassword()
            };
            dtm.addRow(data);
        };
        
        if (hasilPencarian.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tidak ditemukan data dengan kata kunci: " + keyword, 
                    "info", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

