    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if(txtNamaPegawai.getText().trim().isEmpty() || txtEmail.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nama dan Email tidak boleh Kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        //Ngambil data dari form
        String namaPegawai = txtNamaPegawai.getText().trim();
        String email = txtEmail.getText().trim();
        String posisi = comboBoxPosisi.getSelectedItem().toString();
        
        DataPegawai pegawai = new DataPegawai();
        pegawai.setNamaPegawai(namaPegawai);
        pegawai.setEmail(email);
        pegawai.setPosisi(posisi);
        pegawai.setPassword(pegawai.generatePassword());
        
        ControllerDataPegawai controller = new ControllerDataPegawai();
        boolean success = controller.create(pegawai);
        
        if (success) {
           JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
           clearData();
           getData();
        } else {
            JOptionPane.showMessageDialog(null, "Gagal menambahkan data!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabelDataPegawai.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Pilih data pegawai yang ingin diubah!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int idPegawai = Integer.parseInt(tabelDataPegawai.getValueAt(selectedRow, 0).toString());
        String namaPegawai = tabelDataPegawai.getValueAt(selectedRow, 1).toString();
        
        String posisiBaru = comboBoxPosisi.getSelectedItem().toString();
        
        int konfirmasi = JOptionPane.showConfirmDialog(null, 
                "Apakah Anda yakiningin mengubah posisi pegawai \"" + namaPegawai + "\" menjadi \"" + posisiBaru + "\"?",
                "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (konfirmasi != JOptionPane.YES_OPTION) {
            return;
        }
        
        DataPegawai pegawai = new DataPegawai();
        pegawai.setIdPegawai(idPegawai);
        pegawai.setPosisi(posisiBaru);
        
        boolean success = conPegawai.update(pegawai); //ini ada pake dari method di controllerPegawai
        if (success) {
            JOptionPane.showMessageDialog(null, "Posisi Pegawai berhasil diubah!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            getData();
        } else {
            JOptionPane.showMessageDialog(null, "Gagal mengubah posisi pegawai!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabelDataPegawai.getSelectedRow();
        if(selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Pilih data pegawai yang ingin dihapus!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int idPegawai = Integer.parseInt(tabelDataPegawai.getValueAt(selectedRow, 0).toString());
        String namaPegawai = tabelDataPegawai.getValueAt(selectedRow, 1).toString();
        
        //Konfirmasi hapuskah atau tidak
        int konfirmasi = JOptionPane.showConfirmDialog(null, 
                "Apakah Anda yakin ingin menghapus pagawai \"" + namaPegawai + "\"?",
                "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (konfirmasi != JOptionPane.YES_OPTION) {
            return;
        }
        
        //menghapus data pegawai dari databasenya
        boolean success = conPegawai.delete(idPegawai);
        if (success) {
            JOptionPane.showMessageDialog(null, "Data Pegawai berhasil dihapus!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            getData();
        } else {
            JOptionPane.showMessageDialog(null, "Gagal menghapus data pegawai!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

